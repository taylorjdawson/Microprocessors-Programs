//==============================================================================
void led_init()
{
	// R0 = PD5 - PWM
	// G0 = PD6 - PWM
	// B0 = PB1 - PWN
	// R1 = PD4
	// G1 = PB2 - PWN
	// B1 = PB0
	//
	// PORT B
	// 7  6  5  4  3  2  1  0
	// -  -  -  -  -  G0 R0 R1
	//
	// PORT D
	// 7  6  5  4  3  2  1  0
	// -  G0 R0 R1 -  -  -  -
	
	PORTB &= ~0x07;
	DDRB  |= 0x07;
	
	PORTD &= ~0x70;
	DDRD  |= 0x70;
}

//==============================================================================
void led_set(uint8_t led, uint8_t value)
{
	// Make sure the value is sized correctly
	value &= 0x07;
	
	// Call the appropriate led function
	if ( 0 == led ) 
	{
		led0_set(value);
	}
	else {
		led1_set(value);
	}
}


//==============================================================================
void led_set_value(uint8_t value)
{
	// Make sure the value is sized correctly
	value &= 0x03F;
	
	// R0 = PD5 - PWM
	// G0 = PD6 - PWM
	// B0 = PB1 - PWN
	// R1 = PD4
	// G1 = PB2 - PWN
	// B1 = PB0
	
	// R0 - PD5
	if (value & (1 << 5))
	{
		PORTD |= (1 << 5);
	}
	else
	{
		PORTD &= ~(1 << 5);
	}
	
	// G0 - PD6
	if (value & (1 << 6))
	{
		PORTD |= (1 << 6);
	}
	else
	{
		PORTD &= ~(1 << 6);
	}
	
	// B0 - PB1
	if (value & (1 << 1))
	{
		PORTB |= (1 << 1);
	}
	else
	{
		PORTB &= ~(1 << 1);
	}
	
	// R1 - PD4
	if (value & (1 << 4))
	{
		PORTD |= (1 << 4);
	}
	else
	{
		PORTD &= ~(1 << 4);
	}
	
	// G1 - PB2
	if (value & (1 << 2))
	{
		PORTB |= (1 << 2);
	}
	else
	{
		PORTB &= ~(1 << 2);
	}
	
	// B1 - PB0
	if (value & (1 << 0))
	{
		PORTB |= (1 << 0);
	}
	else
	{
		PORTB &= ~(1 << 0);
	}

}
