//==============================================================================
// Turn on or off the appropriate LEDs
// value is expected to be 0b00000RGB
// R0 = PD5 - PWM
// G0 = PD6 - PWM
// B0 = PB1 - PWN
//==============================================================================
static void led0_set(uint8_t value)
{
	// R0 - PD5
	if (value & 0x04)
	{
		PORTD |= (1 << 5);
	}
	else
	{
		PORTD &= ~(1 << 5);
	}
	
	// G0 - PD6
	if (value & 0x02)
	{
		PORTD |= (1 << 6);
	}
	else
	{
		PORTD &= ~(1 << 6);
	}
	
	// B0 - PB1
	if (value & 0x01)
	{
		PORTB |= (1 << 1);
	}
	else
	{
		PORTB &= ~(1 << 1);
	}
	
	
}

//==============================================================================
// Turn on or off the appropriate LEDs
// value is expected to be 0b00000RGB
// R1 = PD4
// G1 = PB2 - PWM
// B1 = PB0
//==============================================================================
static void led1_set(uint8_t value)
{
	// R1 - PD4
	if (value & 0x04)
	{
		PORTD |= (1 << 4);
	}
	else
	{
		PORTD &= ~(1 << 4);
	}
	// G1 - PB2
	if (value & 0x02)
	{
		PORTB |= (1 << 2);
	}
	else
	{
		PORTB &= ~(1 << 2);
	}
	// B1 - PB0
	if (value & 0x01)
	{
		PORTB |= (1 << 0);
	}
	else
	{
		PORTB &= ~(1 << 0);
	}

}	
