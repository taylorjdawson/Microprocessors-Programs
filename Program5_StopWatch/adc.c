//==============================================================================
void adc_init()
{
	ADMUX |= _BV(REFS0); // Set ADC reference to AVCC
	ADCSRA |= _BV(ADEN) | _BV(ADIE) | _BV(ADSC) | _BV(ADATE) | _BV(ADPS2) | _BV(ADPS1) | _BV(ADPS0);
}

