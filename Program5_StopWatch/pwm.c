//==============================================================================
void pwm_init()
{
	// Timer 0
	TCCR0A |= _BV(COM0A1) | _BV(COM0B1) | _BV(WGM01) | _BV(WGM00);
	TCCR0B |= _BV(CS00);
	
	//Settings for 8-bit Fast PWM mode Timer 1
	TCCR1A |= _BV(COM1A1) | _BV(WGM10);
	TCCR1B |= _BV(CS10) | _BV(WGM12);
}

