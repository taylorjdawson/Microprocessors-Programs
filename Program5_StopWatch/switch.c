//=============================================================================
void switch_init()
{
	DDRB &= ~(1 << 4); // Make sure PB4 is an input
	
	// Set up the pin change interrupt on  PBC (PCINT4)
	PCICR |= (1 << PCIE0);
	PCMSK0 |= (1 << PCINT4);
	
	// Set the initial switch_value
	switch_value = (PINB & (1 << 4)) ? true : false;
}

//==============================================================================
// Switch is active high
// Switch is on PB4
//==============================================================================
bool switch_get()
{
	return switch_value;
}

